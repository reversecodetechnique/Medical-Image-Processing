class GradCAM:
    """Grad-CAM implementation for CNN visualization"""

    def __init__(self, model, layer_name=None):
        self.model = model

        if not self.model.built:
            # Create a dummy input tensor with the expected input shape of the model
            # Assumes model.input_shape is available (e.g., (None, H, W, C))
            if self.model.input_shape:
                dummy_input_shape = (1,) + self.model.input_shape[1:]
                _ = self.model(tf.zeros(dummy_input_shape))
            else:
                raise ValueError("Model input shape could not be determined. Ensure model is built (e.g., by calling model.predict() once) or provide input_shape to model construction.")

        # Call find_target_layer as a method of the instance
        self.layer_name = layer_name or self._find_target_layer() # Renamed to _find_target_layer to indicate it's internal
        if self.layer_name is None:
            raise ValueError("No suitable target layer found for Grad-CAM. Please ensure your model has a convolutional layer before the final dense layers, or specify 'layer_name' manually.")


    # Move this function inside the class and make it a method
    def _find_target_layer(self): # Renamed to _find_target_layer and added self
        """Finds the name of the last convolutional layer in the model."""
        for layer in reversed(self.model.layers):
            # Check for Conv2D layers specifically
            if isinstance(layer, layers.Conv2D):
                return layer.name
        # If no Conv2D layer is found after checking all layers in reverse
        return None # Indicate that no suitable layer was found


    def generate_heatmap(self, img_array, class_index=None):
        """Generate Grad-CAM heatmap for a given image array."""

        # Create a model that maps the input image to the activations
        # of the last conv layer as well as the output predictions
        grad_model = Model(
            inputs=[self.model.inputs],
            outputs=[self.model.get_layer(self.layer_name).output, self.model.output]
        )

        # Compute the gradient of the top predicted class for our input image
        # with respect to the activations of the last conv layer
        with tf.GradientTape() as tape:
            conv_outputs, predictions = grad_model(img_array)

            if predictions.shape[-1] == 1: # Binary classification with single sigmoid output
                # For binary classification with a single sigmoid output neuron:
                # If class_index is None, we target the predicted class's probability neuron.
                # If class_index is 0 (Normal), we target 1 - prediction[0,0] (the probability of Normal).
                # If class_index is 1 (Pneumonia), we target prediction[0,0] (the probability of Pneumonia).
                if class_index is None: # Use the predicted class
                    target_class_prob = predictions[:, 0]
                elif class_index == 1: # Explicitly target Pneumonia
                    target_class_prob = predictions[:, 0]
                elif class_index == 0: # Explicitly target Normal
                    target_class_prob = 1 - predictions[:, 0]
                else:
                    raise ValueError("For binary classification, class_index must be 0, 1, or None.")
                class_channel = target_class_prob
            else: # Multi-class classification (softmax output)
                if class_index is None:
                    class_index = tf.argmax(predictions[0])
                class_channel = predictions[:, class_index]

        # Gradient of the output neuron (top predicted or chosen)
        # with regard to the output feature map of the last conv layer
        grads = tape.gradient(class_channel, conv_outputs)

        # Mean intensity of the gradient over a specific feature map channel
        pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))

        # Multiply each channel in the feature map array
        # by "how important this channel is" with regard to the top predicted class
        conv_outputs = conv_outputs[0] # Get rid of batch dimension
        # The '@' operator (matrix multiplication) works here because:
        # conv_outputs is (H, W, C)
        # pooled_grads[..., tf.newaxis] makes pooled_grads (C, 1)
        # Result is (H, W, 1), which is then squeezed.
        heatmap = conv_outputs @ pooled_grads[..., tf.newaxis]
        heatmap = tf.squeeze(heatmap) # Remove the last dimension (1)

        # Normalize the heatmap (ReLU operation to keep only positive gradients)
        heatmap = tf.maximum(heatmap, 0) # Apply ReLU
        max_heatmap_val = tf.math.reduce_max(heatmap)
        # Handle potential division by zero if heatmap is all zeros
        heatmap = tf.where(tf.math.equal(max_heatmap_val, 0), tf.zeros_like(heatmap), heatmap / max_heatmap_val)

        return heatmap.numpy()

    def visualize_heatmap(self, img_path, class_index=None, alpha=0.4, save_path=None):
        """Visualize Grad-CAM heatmap on image"""

        # Load and preprocess image
        # Ensure target_size matches your model's input expected (height, width)
        # If your model takes 3 channels (like ResNet50), but image is grayscale, handle conversion.
        # If IMAGE_SIZE is (W, H, C), img.size is (W, H)
        # Use the global IMAGE_SIZE which should be (256, 256)
        img = tf.keras.preprocessing.image.load_img(img_path, target_size=IMAGE_SIZE[:2])
        img_array = tf.keras.preprocessing.image.img_to_array(img)

        # If the model expects 3 channels but the image is grayscale (1 channel after img_to_array)
        if img_array.shape[-1] == 1 and self.model.input_shape[-1] == 3:
            img_array = np.concatenate([img_array, img_array, img_array], axis=-1)

        # Normalize pixel values to 0-1 range as expected by the model
        img_array_for_model = np.expand_dims(img_array, axis=0) / 255.0

        # Generate prediction
        prediction = self.model.predict(img_array_for_model, verbose=0)

        # Determine predicted class and confidence for display
        if prediction.shape[-1] == 1: # Binary classification
            predicted_class = int(prediction[0][0] > 0.5)
            confidence = prediction[0][0] if predicted_class == 1 else (1 - prediction[0][0])
            # If class_index is not specified for Grad-CAM, use the predicted class
            if class_index is None:
                class_index_for_heatmap = predicted_class
            else:
                class_index_for_heatmap = class_index
        else: # Multi-class classification
            predicted_class = tf.argmax(prediction[0]).numpy()
            confidence = prediction[0][predicted_class]
            if class_index is None:
                class_index_for_heatmap = predicted_class
            else:
                class_index_for_heatmap = class_index

        # Generate heatmap
        heatmap = self.generate_heatmap(img_array_for_model, class_index=class_index_for_heatmap)

        # Resize heatmap to match original image size (width, height)
        # img.size gives (width, height) for PIL images
        heatmap_resized = cv2.resize(heatmap, (img.size[0], img.size[1]))
        # Convert heatmap to a color map (JET is a common choice)
        heatmap_colored = cv2.applyColorMap(np.uint8(255 * heatmap_resized), cv2.COLORMAP_JET)

        # Convert original image to a NumPy array for visualization with OpenCV
        # Ensure it's in BGR format for cv2.addWeighted, and 3 channels
        img_array_vis = np.array(img)
        if img_array_vis.ndim == 2: # Grayscale image
             img_array_vis = cv2.cvtColor(img_array_vis, cv2.COLOR_GRAY2BGR)
        elif img_array_vis.shape[-1] == 1: # Grayscale with 1 channel dim
             img_array_vis = cv2.cvtColor(img_array_vis, cv2.COLOR_GRAY2BGR)
        else: # RGB image
            img_array_vis = cv2.cvtColor(img_array_vis, cv2.COLOR_RGB2BGR) # Convert to BGR for OpenCV


        # Create overlay using cv2.addWeighted for proper blending and type handling
        overlay = cv2.addWeighted(heatmap_colored, alpha, img_array_vis, 1 - alpha, 0)


        # Visualization using Matplotlib
        fig, axes = plt.subplots(1, 4, figsize=(20, 5))

        # Original image (Matplotlib expects RGB, so convert back from BGR if necessary)
        axes[0].imshow(cv2.cvtColor(img_array_vis, cv2.COLOR_BGR2RGB))
        axes[0].set_title('Original Image')
        axes[0].axis('off')

        # Heatmap
        axes[1].imshow(heatmap_resized, cmap='jet')
        axes[1].set_title('Grad-CAM Heatmap')
        axes[1].axis('off')

        # Overlay (Matplotlib expects RGB, so convert back from BGR)
        axes[2].imshow(cv2.cvtColor(overlay, cv2.COLOR_BGR2RGB))
        axes[2].set_title('Grad-CAM Overlay')
        axes[2].axis('off')

        # Prediction info
        class_names = ['Normal', 'Pneumonia'] # Assuming binary classification for pneumonia
        axes[3].text(0.1, 0.7, f'Prediction: {class_names[predicted_class]}',
                             fontsize=14, transform=axes[3].transAxes)
        axes[3].text(0.1, 0.5, f'Confidence: {confidence:.3f}',
                             fontsize=14, transform=axes[3].transAxes)
        axes[3].text(0.1, 0.3, f'Target Layer: {self.layer_name}',
                             fontsize=12, transform=axes[3].transAxes)
        axes[3].axis('off')

