def analyze_sample_images(generator, gradcam_analyzer, model, num_samples=2):
    """Analyze sample images with Grad-CAM and display them.

    Args:
        generator: A Keras ImageDataGenerator flow_from_directory generator
                   (e.g., test_generator) providing batches of images.
        gradcam_analyzer: An instance of the GradCAM class.
        model: The trained Keras model used for predictions.
        num_samples: The number of sample images to display.
    """

    generator.reset() # Reset generator to ensure consistent samples from the start

    all_images_normalized = []
    all_true_labels = []

    # Collect enough samples to meet num_samples, batch by batch
    samples_collected = 0
    batch_count = 0
    while samples_collected < num_samples:
        try:
            # Get the next batch
            batch_x, batch_y = next(generator)
            batch_count += 1

            print(f"--- Collecting Batch {batch_count} ---")
            print(f"Images in current batch: {len(batch_x)}")

            if len(batch_x) == 0:
                print("WARNING: Empty batch received from generator. Breaking loop.")
                break

            all_images_normalized.extend(batch_x)
            all_true_labels.extend(batch_y)

            samples_collected += len(batch_x)
            print(f"Total samples collected so far: {samples_collected}")

        except StopIteration:
            print("Generator exhausted before collecting all desired samples.")
            break
        except Exception as e:
            print(f"Error getting batch from generator: {e}")
            break

    print(f"\nFinished collecting batches. Total images in 'all_images_normalized': {len(all_images_normalized)}")

    # If no samples were collected, return
    if not all_images_normalized:
        print("No samples collected from the generator. Cannot perform analysis.")
        return

    # Use only the first 'num_samples' images if more were collected than needed
    images_to_analyze = all_images_normalized[:num_samples]
    labels_to_analyze = all_true_labels[:num_samples]

    # Adjust num_samples if we collected fewer than requested
    actual_num_samples = len(images_to_analyze)
    if actual_num_samples == 0:
        print("No images available to analyze.")
        return

    print(f"Actual number of samples to plot: {actual_num_samples}")

    fig, axes = plt.subplots(2, actual_num_samples, figsize=(actual_num_samples * 4, 8))

    # Ensure axes is always a 2D array, even for a single image
    if actual_num_samples == 1:
        axes = np.array([axes]).reshape(2, 1)

    class_names = ['Normal', 'Pneumonia'] # Define your class names

    for i in range(actual_num_samples):
        img_normalized = images_to_analyze[i] # Image array, normalized (0-1)
        true_label = int(labels_to_labels[i]) # Corrected line: Use all_true_labels

        # 1. Prepare image for display and OpenCV operations:
        img_display = (img_normalized * 255).astype(np.uint8)

        if img_display.ndim == 2:
             img_display = cv2.cvtColor(img_display, cv2.COLOR_GRAY2BGR)
        elif img_display.shape[2] == 1:
             img_display = cv2.cvtColor(img_display, cv2.COLOR_GRAY2BGR)
        else:
            img_display = cv2.cvtColor(img_display, cv2.COLOR_RGB2BGR)

        # 2. Make prediction using the model (model expects normalized input)
        prediction = model.predict(np.expand_dims(img_normalized, axis=0), verbose=0)

        # Determine predicted class and confidence for binary classification
        predicted_class = int(prediction[0][0] > 0.5)
        confidence = prediction[0][0] if predicted_class == 1 else (1 - prediction[0][0])

        # 3. Generate heatmap using the GradCAM analyzer
        heatmap = gradcam_analyzer.generate_heatmap(np.expand_dims(img_normalized, axis=0))

        # 4. Process heatmap and create overlay:
        heatmap_resized = cv2.resize(heatmap, (img_display.shape[1], img_display.shape[0]))
        heatmap_colored = cv2.applyColorMap(np.uint8(255 * heatmap_resized), cv2.COLORMAP_JET)
        overlay = cv2.addWeighted(heatmap_colored, 0.4, img_display, 0.6, 0)

        # 5. Visualization using Matplotlib
        axes[0, i].imshow(cv2.cvtColor(img_display, cv2.COLOR_BGR2RGB))
        axes[0, i].set_title(f'True: {class_names[true_label]}\n'
                             f'Pred: {class_names[predicted_class]} (Conf: {confidence:.3f})')
        axes[0, i].axis('off')

        axes[1, i].imshow(cv2.cvtColor(overlay, cv2.COLOR_BGR2RGB))
        axes[1, i].set_title('Grad-CAM Overlay')
        axes[1, i].axis('off')

    plt.tight_layout()
    plt.show()

