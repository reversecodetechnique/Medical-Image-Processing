class LungDataset(Dataset):
    def __init__(self, image_paths, mask_dir=None, transform=None, mask_transform=None):
        """
        Initializes the LungDataset.

        Args:
            image_paths (list): List of paths to the images.
            mask_dir (str, optional): Directory containing the corresponding masks. Defaults to None.
            transform (callable, optional): Optional transform to be applied to images. Defaults to None.
            mask_transform (callable, optional): Optional transform to be applied to masks. Defaults to None.
        """
        self.image_paths = image_paths
        self.mask_dir = mask_dir
        self.transform = transform
        self.mask_transform = mask_transform



    def __len__(self):
        # Return the number of samples in your dataset
        return len(self.image_paths)

    def __getitem__(self, idx):
        # Load image and mask
        img_path = self.image_paths[idx]
        image = Image.open(img_path).convert('L') # Open image and convert to grayscale

        mask = None
        if self.mask_dir:
            # Construct mask path based on image path and mask_dir
            filename = os.path.basename(img_path).replace('.jpeg', '.png').replace('.jpg', '.png')
            mask_path = os.path.join(self.mask_dir, filename)
            if os.path.exists(mask_path):
                mask = Image.open(mask_path).convert('L') # Open mask and convert to grayscale

        # Apply transforms
        if self.transform:
            image = self.transform(image)

        if self.mask_transform and mask is not None:
            mask = self.mask_transform(mask)

        # Return the image and mask (or just image if no mask_dir)
        if mask is not None:
            return image, mask
        else:
            return image

