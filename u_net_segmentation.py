def predict_and_visualize(model, image_path, device='cuda', threshold=0.5):
    """
    Predicts segmentation mask and visualizes results.
    """
    model.eval() # Set model to evaluation mode
    # Image transformation for prediction
    transform_predict = transforms.Compose([
        transforms.Resize((256, 256)), # Resize to model input size
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485], std=[0.229])
    ])

    # Load and preprocess image
    image = Image.open(image_path).convert('L') # Convert to grayscale
    original_size = image.size # Store original size for display

    image_tensor = transform_predict(image).unsqueeze(0).to(device) # Add batch dimension and move to device

    with torch.no_grad():
        output = model(image_tensor)
        pred_mask = torch.sigmoid(output).squeeze(0) # Apply sigmoid and remove batch dimension

        pred_mask = (pred_mask > threshold).float() # Binarize the mask based on threshold

    # Convert to numpy for visualization
    image_np = image_tensor.squeeze().cpu().numpy() # Remove batch and channel dims, move to CPU, convert to numpy
    pred_mask_np = pred_mask.squeeze().cpu().numpy() # Squeeze, move to CPU, convert to numpy

    # Plot results
    fig, axes = plt.subplots(1, 3, figsize=(15, 5))

    # Original Image
    axes[0].imshow(image_np, cmap='gray')
    axes[0].set_title('Original Image')
    axes[0].axis('off')

    # Predicted Mask
    axes[1].imshow(pred_mask_np, cmap='gray')
    axes[1].set_title('Predicted Mask')
    axes[1].axis('off')

    # Overlay (optional: you might need to adjust alpha for better visibility)
    # Convert grayscale image to RGB to overlay mask
    image_rgb = cv2.cvtColor((image_np * 255).astype(np.uint8), cv2.COLOR_GRAY2RGB)
    overlay = image_rgb.copy()
    # Apply mask in red color
    overlay[pred_mask_np == 1] = [255, 0, 0] # Red color for segmented area

    alpha = 0.5 # Transparency of overlay
    final_overlay = cv2.addWeighted(image_rgb, 1 - alpha, overlay, alpha, 0)

    axes[2].imshow(final_overlay.astype(np.uint8))
    axes[2].set_title('Overlay')
    axes[2].axis('off')

    plt.tight_layout()
    plt.show() # Display the plot

    return pred_mask_np

