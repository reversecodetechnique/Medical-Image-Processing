def create_synthetic_lung_masks(image_paths, mask_dir, image_size=(256, 256)):
    """
    Creates synthetic lung masks using simple thresholding and morphological operations.
    Saves the masks to mask_dir.
    """
    os.makedirs(mask_dir, exist_ok=True)
    from skimage.filters import threshold_otsu
    from skimage import morphology

    for img_path in image_paths:
        filename = os.path.basename(img_path)
        mask_filepath = os.path.join(mask_dir, filename.replace('.jpeg', '.png').replace('.jpg', '.png')) # Ensure mask is png

        if os.path.exists(mask_filepath):
            continue # Skip if mask already exists

        # Read image
        img = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE)
        if img is None:
            # print(f"Warning: Could not read image {img_path}")
            continue

        img_resized = cv2.resize(img, image_size)

        # Apply Gaussian Blur
        blurred = cv2.GaussianBlur(img_resized, (5, 5), 0)

        # Threshold
        thresh = threshold_otsu(blurred)
        binary = blurred > thresh

        # Remove small objects and fill holes
        binary = morphology.remove_small_objects(binary, min_size=1000) # Adjust min_size as needed
        binary = morphology.remove_small_holes(binary, area_threshold=1000) # Adjust area_threshold

        # Convert to 0-255 range
        mask = (binary * 255).astype(np.uint8)

        # Save mask
        cv2.imwrite(mask_filepath, mask)
    print(f"Created/Checked {len(image_paths)} synthetic masks in {mask_dir}")


